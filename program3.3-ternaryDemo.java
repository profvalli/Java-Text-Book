// Program 3.3 : Using Ternary operator
public class ternaryDemo
{
  public static void main (String[]args)
  {

    int i, result1, result2;
      i = 1;
      result1 = (i == 1) ? 1 : 0;
      System.out.println ("result is: " + result1);
      result2 = (i == 2) ? 1 : 0;
      System.out.println ("result is: " + result2);
  }
}

// output: 
//result is: 1
//result is: 0
